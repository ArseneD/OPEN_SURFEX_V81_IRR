SUBROUTINE ERROR_READ_SURF_LFI(HREC,KRESP)
!
USE MODI_ABOR1_SFX
!
USE MODD_IO_SURF_LFI, ONLY : NLUOUT
!
USE YOMHOOK   ,ONLY : LHOOK,   DR_HOOK
USE PARKIND1  ,ONLY : JPRB
!
IMPLICIT NONE
!
 CHARACTER(LEN=*), INTENT(IN) :: HREC
INTEGER,          INTENT(IN) :: KRESP
REAL(KIND=JPRB) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('ERROR_READ_SURF_LFI',0,ZHOOK_HANDLE)
!
IF (KRESP==0 .AND. LHOOK) CALL DR_HOOK('ERROR_READ_SURF_LFI',1,ZHOOK_HANDLE)
IF (KRESP==0) RETURN
!
WRITE(NLUOUT,*) ' '
WRITE(NLUOUT,*) 'WARNING'
WRITE(NLUOUT,*) '-------'
WRITE(NLUOUT,*) ' '
WRITE(NLUOUT,*) 'error when reading article', HREC
WRITE(NLUOUT,*) "default value may be used; who knows?"
WRITE(NLUOUT,*) ' '
!
 CALL ABOR1_SFX('ERROR_READ_SURF_LFI: ERROR WHEN READING ARTICLE '//HREC)
IF (LHOOK) CALL DR_HOOK('ERROR_READ_SURF_LFI',1,ZHOOK_HANDLE)
!
END SUBROUTINE ERROR_READ_SURF_LFI
