  YRECFM=ADJUSTL(YPATCH//YRECFM)
  YCOMMENT='net radiation for built surfaces'//YRECFM//' (W/m2)'
  CALL WRITE_SURF(HSELECT,HPROGRAM,YRECFM,DMT%XRN_BLT(:),IRESP,HCOMMENT=YCOMMENT)
